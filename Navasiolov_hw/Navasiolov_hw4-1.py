a = int(input("Введиче число (а): "))
b = int(input("Введите число (б): "))
print("\n Сумма а и b:", a + b)
print(" Разница между a и b:",  a - b)
print(" Произведение a и b:", a * b)
print(" Частное от деления a на b:", a / b)
print(" Отстаток от деления a на b:", a % b)
print(" Результат возведения числа в степень b:", a ** b)
